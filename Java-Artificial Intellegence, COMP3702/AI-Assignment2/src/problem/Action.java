package problem;

/**
 * Enumeration of possible actions. 
 * FS: forward slow
 * FM: forward medium
 * FF: forward fast
 * NE: north-east
 * SE: south-east
 * ST: stay
 * TO: turn into an obstacle
 * TC: turn back into a cycle
 */
public enum Action {
	FS, FM, FF, NE, SE, ST, TO, TC
}
